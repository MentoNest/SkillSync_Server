import { INestApplication } from '@nestjs/common';
import { Test, TestingModule } from '@nestjs/testing';
import * as request from 'supertest';
import { AppModule } from '../src/app.module';

describe('API Contract Tests (e2e)', () => {
  let app: INestApplication;

  beforeAll(async () => {
    const moduleFixture: TestingModule = await Test.createTestingModule({
      imports: [AppModule],
    }).compile();

    app = moduleFixture.createNestApplication();
    app.setGlobalPrefix('api');
    await app.init();
  });

  afterAll(async () => {
    await app.close();
  });

  // ── Health Check Contract ─────────────────────────────────────────────

  describe('GET /api/v1/health', () => {
    it('should return 200 with a valid health response shape', async () => {
      const response = await request(app.getHttpServer())
        .get('/api/v1/health')
        .expect(200);

      // Contract: response must be an object
      expect(response.body).toBeDefined();
      expect(typeof response.body).toBe('object');
    });
  });

  // ── Authentication Contract ───────────────────────────────────────────

  describe('POST /api/v1/auth/nonce', () => {
    const VALID_PUBLIC_KEY = 'GA7QYNF7SOWQ3GLR2BGMQHXCZ3FKEOGXGMNHQV3U6K6DGQFG3ZQVEXAMPLE';

    it('should return 201 with a nonce for a valid public key', async () => {
      const response = await request(app.getHttpServer())
        .post('/api/v1/auth/nonce')
        .send({ publicKey: VALID_PUBLIC_KEY })
        .expect(201);

      // Contract: must return { nonce: string }
      expect(response.body).toHaveProperty('nonce');
      expect(typeof response.body.nonce).toBe('string');
    });

    it('should return 400 for missing public key', async () => {
      await request(app.getHttpServer())
        .post('/api/v1/auth/nonce')
        .send({})
        .expect(400);
    });
  });

  // ── User Profile Contract ─────────────────────────────────────────────

  describe('GET /api/v1/users/me', () => {
    it('should return 401 without authentication', async () => {
      await request(app.getHttpServer())
        .get('/api/v1/users/me')
        .expect(401);
    });

    it('should return 401 with invalid token', async () => {
      await request(app.getHttpServer())
        .get('/api/v1/users/me')
        .set('Authorization', 'Bearer invalid-token')
        .expect(401);
    });
  });

  // ── Stellar Wallet Validation Contract ─────────────────────────────────

  describe('POST /api/v1/auth/login', () => {
    it('should return 400 for invalid Stellar public key format', async () => {
      await request(app.getHttpServer())
        .post('/api/v1/auth/login')
        .send({
          publicKey: 'not-a-valid-key',
          signature: 'invalid-signature',
        })
        .expect(400);
    });
  });

  // ── Pagination Contract ───────────────────────────────────────────────

  describe('GET /api/v1/users', () => {
    it('should return 200 with paginated response shape', async () => {
      const response = await request(app.getHttpServer())
        .get('/api/v1/users?page=1&limit=10')
        .expect(200);

      // Response should be an object or array
      expect(response.body).toBeDefined();
    });
  });
});

